public class GadgetListBrandData {
    private String text_name;
    private String key_name;
    private String text_age;
    private String key_age;
    private String text_price;
    private String key_price;

    public String getText_name() {
        return text_name;
    }

    public void setText_name(String text_name) {
        this.text_name = text_name;
    }

    public String getKey_name() {
        return key_name;
    }

    public void setKey_name(String key_name) {
        this.key_name = key_name;
    }

    public String getText_age() {
        return text_age;
    }

    public void setText_age(String text_age) {
        this.text_age = text_age;
    }

    public String getKey_age() {
        return key_age;
    }

    public void setKey_age(String key_age) {
        this.key_age = key_age;
    }

    public String getText_price() {
        return text_price;
    }

    public void setText_price(String text_price) {
        this.text_price = text_price;
    }

    public String getKey_price() {
        return key_price;
    }

    public void setKey_price(String key_price) {
        this.key_price = key_price;
    }
}
